//97. Calcular el área de un triángulo a partir de base y altura.
#include <iostream>
using namespace std;
int main() {
    double base, altura;
    cout << "Base: "; cin >> base;
    cout << "Altura: "; cin >> altura;
    double area = (base * altura) / 2;
    cout << "Área del triángulo: " << area << endl;
    return 0;
}











