//67. Calcular la energía cinética.
#include <iostream>
using namespace std;
int main() {
    double masa, velocidad;
    cout << "Masa (kg): "; cin >> masa;
    cout << "Velocidad (m/s): "; cin >> velocidad;
    cout << "Energía cinética: " << 0.5 * masa * velocidad * velocidad << " J" << endl;
    return 0;
}

















